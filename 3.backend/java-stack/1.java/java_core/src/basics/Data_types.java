package basics;

public class Data_types {

	public static void main(String[] args) {
		
		byte class_attendance_num = 21;
		long exam_roll_num = 34641654135L;
		String name = "Bharath";
		int total_marks = 486;
		float avg_marks = 97.2F;
		double cut_off = 98.321654131;
		char grade = 'S';
		short all_india_rank = 1024;
		boolean pass = true;
		
		System.out.println("Attendance number: "+class_attendance_num);
		System.out.println("Roll number: "+exam_roll_num);
		System.out.println("Name: "+name);
		System.out.println("Total Marks: "+total_marks);
		System.out.println("Average Marks "+avg_marks);
		System.out.println("Cut-off: "+cut_off);
		System.out.println("Grade: "+grade);
		System.out.println("All India Rank: "+all_india_rank);
		System.out.println("Result: "+pass);
	}

}
